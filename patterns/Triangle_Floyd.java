package patterns;

public class Triangle_Floyd {

    static void sol(int n) {
        int num = 1;
        for (int i = 1; i <= n; i++) {
            for (int j = 1; j <= i; j++) {
                System.out.print(num + " ");
                num++;
            }
            System.out.print("\n");
        }

    }

}
